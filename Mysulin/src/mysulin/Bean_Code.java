package mysulin;


public class Bean_Code {
	
	private int    numb;
	private String usid;
	private String agnt;
	private String code;
	private String name;
	private String note;
	
	public void setNumb(int numb)	   {this.numb = numb;}
	public void setUsid(String usid)   {this.usid = usid;}
	public void setAgnt(String agnt)   {this.agnt = agnt;}
	public void setCode(String code)   {this.code = code;}
	public void setName(String name)   {this.name = name;}
	public void setNote(String note)   {this.note = note;}
	
	public int getNumb() 	  {return numb;}
	public String getUsid()   {return usid;}
	public String getAgnt()   {return agnt;}
	public String getCode()   {return code;}
	public String getName()   {return name;}
	public String getNote()   {return note;}

}
